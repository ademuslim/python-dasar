#~~~OPERASI KOMPARASI~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
#>,<,>=,<=, ==, !=, is, is not
print ('\nOPERASI KOMPARASI________')

a = 4
b = 2   #<--setiap hasil dari komprasi adalah boolean (true/false) a > b = True 

print ('\n________ > ________')
#lebih besar dari (>)
ade = a > b
print (a,">",b,"=", ade)

#kurang dari (<)
print ('\n________ < ________')

ade = a < b
print (a,"<",b,"=", ade)

#lebih besar sama dengan (>=)
print ('\n________ >= _______')

ade = a >= b
print (a,">=",b,"=", ade)

#kurang dari sama dengan (<=)
print ('\n________ <= _______')

ade = a <= b
print (a,"<=",b,"=", ade)

#sama dengan (==)
print ('\n________ == _______')

ade = a == b
print (a,"==",b,"=", ade)

#is (is untuk membandingkan sesama variable)
print ('\n________ is _______')

ade = a is b
print (a,"is",b,"=", ade)

#is not
print ('\n______ is not _____')

ade = a is not b   #<---a is not b
print (a,"is not",b,"=", ade)


print ('\n-->Sumber Kelas Terbuka')



